def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def distance(point1, point2):
    "Calculate the distance between two points."
    x1, y1 = point1
    x2, y2 = point2
    return abs(x1 - x2) + abs(y1 - y2)

def get_direction(point1, point2):
    "Determine the direction vector from point1 to point2."
    x1, y1 = point1
    x2, y2 = point2
    x_diff = (x2 - x1) // 20  # Integer division for grid movement
    y_diff = (y2 - y1) // 20
    return vector(x_diff, y_diff)

def check_wall_collision(point, course):
    """
    Check if the next move would lead to a wall collision.
    Returns a new course if a collision is detected, otherwise the original course.
    """
    next_point = point + course
    index = offset(next_point)

    if not valid(next_point):
        # Collision detected, reverse direction in the colliding axis
        if course.x != 0:
            course.x = -course.x
        else:
            course.y = -course.y

    return course

def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for i in range(len(ghosts)):
        point, course = ghosts[i]
        # Calculate the direction to chase PacMan
        chase_direction = get_direction(point, pacman)

        # Check for wall collision and adjust course if necessary
        course = check_wall_collision(point, course)

        # If the current course isn't valid, choose a random direction
        if not valid(point + course):
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        # Prioritize chasing PacMan over random movement
        if distance(point + chase_direction, pacman) < distance(point + course, pacman):
            course.x = chase_direction.x
            course.y = chase_direction.y

        point.move(course)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 150)

def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


